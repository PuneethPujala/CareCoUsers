/**
 * Get a random template from a specific category
 */
export const getRandomTemplate = (category, subcategory = null) => {
    const section = NOTIFICATION_TEMPLATES[category];
    if (!section) return "";
    
    // If it's an array directly (like familyBridge, weather, welcome)
    if (Array.isArray(section)) {
        return section[Math.floor(Math.random() * section.length)];
    }
    
    // If it's an object with subcategories
    if (subcategory && section[subcategory]) {
        const templates = section[subcategory];
        return templates[Math.floor(Math.random() * templates.length)];
    }
    
    return "";
};

/**
 * Replace personalization tokens inside the template
 */
export const personalize = (template, tokens = {}) => {
    let result = template;
    
    if (tokens.name) {
        result = result.replace(/\[Name\]/gi, tokens.name.split(' ')[0] || '');
        result = result.replace(/\[Caretaker Name\]/gi, tokens.caretakerName || 'Your Caretaker');
        result = result.replace(/\[Caretaker\]/gi, tokens.caretakerName || 'Your Caretaker');
        result = result.replace(/\[Driver Name\]/gi, tokens.driverName || 'Your Driver');
        result = result.replace(/\[Doctor Name\]/gi, tokens.doctorName || 'Your Doctor');
    } else {
        result = result.replace(/\[Name\],\s*/gi, ''); // Remove trailing comma if name is missing
        result = result.replace(/\[Name\]/gi, 'there');
    }

    if (tokens.medicineName) {
        result = result.replace(/\[Medicine Name\]/gi, tokens.medicineName);
        result = result.replace(/\[MedicineName\]/gi, tokens.medicineName);
        result = result.replace(/\[Medicine A\]/gi, tokens.medicineA || tokens.medicineName);
        result = result.replace(/\[Medicine B\]/gi, tokens.medicineB || 'your other medications');
    }

    if (tokens.current) result = result.replace(/\[Current\]/gi, tokens.current);
    if (tokens.xx) result = result.replace(/\[XX\]/gi, tokens.xx);
    if (tokens.x) result = result.replace(/\[X\]/gi, tokens.x);
    if (tokens.date) result = result.replace(/\[Date\]/gi, tokens.date);
    if (tokens.time) result = result.replace(/\[Time\]/gi, tokens.time);
    if (tokens.metric) result = result.replace(/\[Metric\]/gi, tokens.metric);
    if (tokens.symptom) result = result.replace(/\[Symptom\]/gi, tokens.symptom);
    if (tokens.hobby) result = result.replace(/\[hobby\]/gi, tokens.hobby);
    if (tokens.term) result = result.replace(/\[Term\]/gi, tokens.term);
    if (tokens.funFact) result = result.replace(/\[Fun Fact\]/gi, tokens.funFact);
    
    return result;
};
